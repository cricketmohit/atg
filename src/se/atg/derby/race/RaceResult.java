package se.atg.derby.race;

public class RaceResult {
	public String victoryMargin	;
	public String scratchings;
	public String getVictoryMargin() {
		return victoryMargin;
	}
	public void setVictoryMargin(String victoryMargin) {
		this.victoryMargin = victoryMargin;
	}
	public String getScratchings() {
		return scratchings;
	}
	public void setScratchings(String scratchings) {
		this.scratchings = scratchings;
	}

}
